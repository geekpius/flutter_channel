class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;
  static const batteryChannel = MethodChannel('com.example.flutter_channel/battery');

  String batteryLevel = 'Waiting...';

  void _incrementCounter() async{
    // setState(() {
    //   _counter++;
    // });
    await getBatteryLevel();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(widget.title),
      ),
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: <Widget>[
            const Text(
              'GET IOS BATTER LEVEL',
            ),
            Text(
              batteryLevel,
              style: Theme.of(context).textTheme.headlineSmall,
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
        child: const Icon(Icons.add),
      ), // This trailing comma makes auto-formatting nicer for build methods.
    );
  }

  
  Future getBatteryLevel() async{
    try{
      final platform = {'ios': 'iOS'};
      final newBatteryLevel = await batteryChannel.invokeMethod('getBatteryLevel', platform);
      setState(() => batteryLevel = '$newBatteryLevel');
    }on PlatformException catch(e){
      print(e);
    }
  }

}
